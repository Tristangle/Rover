package Carte;

public class Arbre {

    // Point de vie de l'arbre
    int vie = 1;
    // Position de l'arbre
    int positionX = 0;
    int positionY = 0;

    public Arbre(int vie, int positionX, int positionY) {
        this.vie = vie;
        this.positionX = positionX;
        this.positionY = positionY;
    }
}
